/**
 * 将角色风格转换为情绪基调
 * 用于 Step3 音乐匹配
 */
export function mapRoleStyleToEmotion(style: RoleStyleCategory): EmotionToneCategory {
  const emotions = ROLE_STYLE_TO_EMOTION_MAP[style];
  return emotions[0] ?? EMOTION_TONE_CATEGORY.SUNNY;
}

/**
 * 批量转换角色风格到情绪基调
 */
export function mapRoleStylesToEmotions(styles: RoleStyleCategory[]): EmotionToneCategory[] {
  const emotionSet = new Set<EmotionToneCategory>();
  for (const style of styles) {
    const mapped = ROLE_STYLE_TO_EMOTION_MAP[style] ?? [EMOTION_TONE_CATEGORY.SUNNY];
    for (const emotion of mapped) {
      emotionSet.add(emotion);
    }
  }
  return Array.from(emotionSet).slice(0, 4);
}

/**
 * 柔性解析风格词数组
 * 将自由文本风格词转换为有效的角色风格枚举
 * 无法解析时使用默认值
 */
export function normalizeRoleStyleWords(words: unknown): RoleStyleCategory[] {
  if (!Array.isArray(words)) {
    return [ROLE_STYLE_CATEGORY.REFRESHING];
  }

  const result: RoleStyleCategory[] = [];
  for (const word of words) {
    if (typeof word !== 'string') continue;
    const parsed = parseRoleStyleFromText(word);
    if (parsed) {
      result.push(parsed);
    }
  }

  return result.length > 0 ? result.slice(0, 4) : [ROLE_STYLE_CATEGORY.REFRESHING];
}

